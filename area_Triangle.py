#find the area of the Triangle 

import math # import math module for use square-root

s1=int(input("enter the one side: "))
s2=int(input("enter the second side: "))
s3=int(input("enter the third side: "))

s=(s1+s2+s3)/2 #find the s

area=math.sqrt(s*s(s-s1)*(s-s2)*(s-s3)) #we use here math.sqrt for getting the square root of the number