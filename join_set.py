'''
    Wap to find the union of the two sets & intersection of two sets
'''

set1={1,2,3,4,5}
set2={5,6,7,8,9,10}

join=set1.union(set2) #union method is used for join the two sets

intersection=set1.intersection(set2) #intersection method is used for find the common element in between two sets 

print(join) #print the union of the two sets

print(intersection) #print the intersection of the two sets